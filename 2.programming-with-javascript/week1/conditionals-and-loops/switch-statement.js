var place = 'first';

switch(place) {
    case 'first':
        console.log('Gold');
        break;
    case 'second':
        console.log('Silver');
        break;
    case 'third':
        console.log('Bronze');
        break;
    default:
        console.log('No medal');
}

var light = "green";

switch(light) {
    case "green":
        console.log("Go!");
        break;
    case "yellow":
        console.log("Slow down!");
        break;
    case "red":
        console.log("Stop!");
        break;
    default:
        console.log("Proceed with caution.");
}
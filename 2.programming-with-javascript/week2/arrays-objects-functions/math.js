// Number Constants

// PI number
const PI = Math.PI;
console.log("PI: ", PI); // 3.14159...

// euler's number
const E = Math.E;
console.log("Euler's number: ", E); // 2.71828...

// natural logaritm of 2
const LN2 = Math.LN2;
console.log("Natural logarithm of 2: ", LN2); // 0.69315...

// Rounding Methods

// rounds up to the closest integer
console.log("Ceiling of PI: ", Math.ceil(PI)); // 4

// rounds down to the closest integer
console.log("Floor of PI: ", Math.floor(PI)); // 3

// rounds up to the closest integer if the decimal is greater than or equal to .5
console.log("Rounded value of PI: ", Math.round(PI)); // 3

// trims off the decimal
console.log("Truncated value of LN2: ", Math.trunc(LN2)); // 0

// Arithmetic and Calculus Methods

// calculates the number 2 to the power of 3
console.log("2 to the power of 3: ", Math.pow(2, 3)); // 8

// calculates the square root of 16
console.log("Square root of 16: ", Math.sqrt(16)); // 4

// calculate the cube root of 8
console.log("Cube root of 8: ", Math.cbrt(8)); // 2

// calculates the absolute value of -10
console.log("Absolute value of -10: ", Math.abs(-10)); // 10

// logarithm of 10 in base 10
console.log("Logarithm of 10: ", Math.log(10)); // 2.30258...

// logarithm of 10 in base 2
console.log("Logarithm of 10 in base 2: ", Math.log2(10)); // 3.32192...

// return the minimum and maxiumum values
console.log("Minimum value: ", Math.min(1, 2, 3, 4, 5)); // 1
console.log("Maximum value: ", Math.max(1, 2, 3, 4, 5)); // 5

// trigonometric methods
console.log("Sine of 0: ", Math.sin(0)); // 0
console.log("Cosine of 0: ", Math.cos(0)); // 1
console.log("Tangent of 0: ", Math.tan(0)); // 0

// random number between 0 and 1
console.log("Random number between 0 and 1: ", Math.random()); // 0.12345...

// random number between 0 and 10
console.log("Random number between 0 and 10: ", Math.random() * 10); // 1.2345...

